    public void CargarTabla(){        
        inv.Recuperar();
        if(inv.getLs()!=null){
            ls = inv.getLs();
            NodoDispositivo nodo = new NodoDispositivo();
            nodo = ls.getP();
            while(nodo!=null){
                disp = nodo.getDispo();
                info[0] = disp.getId();
                info[1] = String.valueOf(disp.getCantidad());
                info[2] = disp.getNombre();
                info[3] = disp.getGama();
                info[4] = disp.getMarca();
                info[5] = String.valueOf(disp.getPrecioBase());                
                System.out.println(info[0]+"  "+info[1]+"  "+info[2]+"   "+info[3]+"   "+info[4]+" "+info[5]);
                modelo.addRow(info);
                nodo = nodo.getSig();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"No existen dispositivos registrados");
        }
            
       
    }   
