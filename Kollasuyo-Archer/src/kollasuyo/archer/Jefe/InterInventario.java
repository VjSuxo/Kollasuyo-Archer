    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        int fila = tabla.getSelectedRow();
        
        if(fila>=0){
            
            String id = tabla.getValueAt(fila,0).toString();
            ls.eliminar(id);
            inv.setLs(ls);
            inv.Guardar();
            
            modelo.removeRow(fila);            
        }
        else{
            JOptionPane.showMessageDialog(null,"Seleccionar Fila");
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        // TODO add your handling code here:
        trabajo = "modificar";
        int fila = tabla.getSelectedRow();
        if(fila>=0){
            Activar();
            this.id.setText(tabla.getValueAt(fila,0).toString());
            this.Cant.setValue(Integer.parseInt(tabla.getValueAt(fila,1).toString()));
            this.Gama.setText(tabla.getValueAt(fila,3).toString());
            this.Marca.setText(tabla.getValueAt(fila,4).toString());
            this.Modelo.setText(tabla.getValueAt(fila,2).toString());
            this.Precio.setText(tabla.getValueAt(fila,5).toString());
            
            
        }
        else{
            JOptionPane.showMessageDialog(null,"Seleccione un elemento de la tabla");
        }
        
        
        
    }//GEN-LAST:event_modificarActionPerformed

