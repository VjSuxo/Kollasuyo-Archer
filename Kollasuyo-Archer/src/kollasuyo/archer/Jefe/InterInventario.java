    private void finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarActionPerformed
        // TODO add your handling code here:
        if("agregar".equals(trabajo)){
        disp = new dispositivo(this.id.getText(), this.Marca.getText(),this.Gama.getText(),Integer.parseInt(this.Cant.getValue().toString()),Double.parseDouble(this.Precio.getText()), this.Modelo.getText());
        ls.adifinal(disp);
        inv.Guardar();                  
         info[0] = this.id.getText();
                info[1] = String.valueOf(disp.getCantidad());
                info[2] = disp.getNombre();
                info[3] = disp.getGama();
                info[4] = disp.getMarca();
                info[5] = String.valueOf(disp.getPrecioBase());                
                System.out.println(info[0]+"  "+info[1]+"  "+info[2]+"   "+info[3]+"   "+info[4]+" "+info[5]);
                modelo.addRow(info);
        }
        if("modificar".equals(trabajo)){
        
            dispositivo disp = new dispositivo(this.id.getText(), this.Marca.getText(),this.Gama.getText(),(int)this.Cant.getValue(),Double.parseDouble(this.Precio.getText()),this.Modelo.getText());
            ls.Modificar(this.id.getText(), disp);
            inv.Guardar();
            int fila = tabla.getSelectedRow();
            tabla.setValueAt(this.id.getText(),fila,0);
            tabla.setValueAt(this.Cant.getValue(),fila,1);
            tabla.setValueAt(this.Modelo.getText(),fila,2);
            tabla.setValueAt(this.Gama.getText(),fila,3);
            tabla.setValueAt( this.Marca.getText(),fila,4);
            tabla.setValueAt(this.Precio.getText(),fila,5);
            
        }
               
        this.id.setText("");
                this.Gama.setText("");
                this.Marca.setText("");
                this.Modelo.setText("");
                this.Precio.setText("");
                Desactivar();
        
        
                
    }//GEN-LAST:event_finalizarActionPerformed

